    void fuzzyfica(){
        fis.setVariable("distancia", sensorDistancia);
        fis.setVariable("velocidade", sensorVelocidade);
        fis.evaluate();
        Variable resp = fis.getVariable("pressaoFreios");
        Double saida = resp.getValue();
        jLabelVelocidade.setText(String.valueOf(saida));
        
        dialog.repaint();
    }

